public class BooleanDemo4 {
    public static void main(String[] args) {
        int age =15;
        int year = 14;
        System.out.println(age < year); // true
        System.out.println(age <= year); // true
        System.out.println(age > year); // false
        System.out.println(age >= year); // false
        System.out.println(age == year); // false
        System.out.println(age != year); // true
        }
    
}
